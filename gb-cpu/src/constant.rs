pub const STAT_INT: u8 = 1 << 1;
/// Mask for TIMER interrupt
pub const TIMER_INT: u8 = 1 << 2;
/// Mask for SERIAL interrupt
pub const SERIAL_INT: u8 = 1 << 3;
/// Mask for JOYPAD interrupt
pub const JOYPAD_INT: u8 = 1 << 4;
